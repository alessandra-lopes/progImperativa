//para adicionar elementos ao final da array: método push()
let cores = ["azul", "branco", "vermelho", "verde"];
cores.push("violeta", "amarelo");
console.log(cores);
// retorna: [ 'azul', 'branco', 'vermelho', 'verde', 'violeta', 'amarelo' ]

//para retirar o último elemento da array: método pop()
let frutas = [ 'romã', 'banana', 'melão', 'maçã'];
frutas.pop(); //modifica a array tanto no comprimento quanto no nº de índices

